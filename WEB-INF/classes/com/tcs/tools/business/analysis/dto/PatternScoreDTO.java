package com.tcs.tools.business.analysis.dto;

public class PatternScoreDTO {
	private String patternScore="";
	private String patternFormat ="";
	private String patternId ="";
	/**
	 * @return the patternScore
	 */
	public String getPatternScore() {
		return patternScore;
	}
	/**
	 * @param patternScore the patternScore to set
	 */
	public void setPatternScore(String patternScore) {
		this.patternScore = patternScore;
	}
	/**
	 * @return the patternFormat
	 */
	public String getPatternFormat() {
		return patternFormat;
	}
	/**
	 * @param patternFormat the patternFormat to set
	 */
	public void setPatternFormat(String patternFormat) {
		this.patternFormat = patternFormat;
	}
	/**
	 * @return the patternId
	 */
	public String getPatternId() {
		return patternId;
	}
	/**
	 * @param patternId the patternId to set
	 */
	public void setPatternId(String patternId) {
		this.patternId = patternId;
	}

}
